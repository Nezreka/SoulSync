#!/bin/bash
# SoulSync Docker Entrypoint Script
# Handles PUID/PGID/UMASK configuration for proper file permissions

set -e

# Default values
PUID=${PUID:-1000}
PGID=${PGID:-1000}
UMASK=${UMASK:-022}

echo "ğŸ³ SoulSync Container Starting..."
echo "ğŸ“ User Configuration:"
echo "   PUID: $PUID"
echo "   PGID: $PGID"
echo "   UMASK: $UMASK"

# Get current soulsync user/group IDs
CURRENT_UID=$(id -u soulsync)
CURRENT_GID=$(id -g soulsync)

# Only modify user/group if they differ from requested values
if [ "$CURRENT_UID" != "$PUID" ] || [ "$CURRENT_GID" != "$PGID" ]; then
    echo "ğŸ”§ Adjusting user permissions..."

    # Modify group ID if needed
    if [ "$CURRENT_GID" != "$PGID" ]; then
        echo "   Changing group ID from $CURRENT_GID to $PGID"
        groupmod -o -g "$PGID" soulsync
    fi

    # Modify user ID if needed
    if [ "$CURRENT_UID" != "$PUID" ]; then
        echo "   Changing user ID from $CURRENT_UID to $PUID"
        usermod -o -u "$PUID" soulsync
    fi

    # Fix ownership of app directories
    echo "ğŸ”’ Fixing permissions on app directories..."
    chown -R soulsync:soulsync /app/config /app/data /app/logs /app/downloads /app/Transfer 2>/dev/null || true
else
    echo "âœ… User/Group IDs already correct"
fi

# Set umask for file creation permissions
echo "ğŸ­ Setting UMASK to $UMASK"
umask "$UMASK"

# Initialize config files if they don't exist (first-time setup)
echo "ğŸ” Checking for configuration files..."

if [ ! -f "/app/config/config.json" ]; then
    echo "   ğŸ“„ Creating default config.json..."
    cp /defaults/config.json /app/config/config.json
    chown soulsync:soulsync /app/config/config.json
else
    echo "   âœ… config.json already exists"
fi

if [ ! -f "/app/config/settings.py" ]; then
    echo "   ğŸ“„ Creating default settings.py..."
    cp /defaults/settings.py /app/config/settings.py
    chown soulsync:soulsync /app/config/settings.py
else
    echo "   âœ… settings.py already exists"
fi

# Ensure all directories exist and have proper permissions
mkdir -p /app/config /app/data /app/logs /app/downloads /app/Transfer
chown -R soulsync:soulsync /app/config /app/data /app/logs /app/downloads /app/Transfer

echo "âœ… Configuration initialized successfully"

# Display final user info
echo "ğŸ‘¤ Running as:"
echo "   User: $(id -u soulsync):$(id -g soulsync) ($(id -un soulsync):$(id -gn soulsync))"
echo "   UMASK: $(umask)"
echo ""
echo "ğŸš€ Starting SoulSync Web Server..."

# Execute the main command as the soulsync user
exec gosu soulsync "$@"
